    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectedRowIndex = jTable1.getSelectedRow();

    // Cek jika baris telah dipilih
    if (selectedRowIndex >= 0) {
        // Aktifkan tombol Update jika baris dipilih
        btnUpdate.setEnabled(true);

        // Ambil data dari baris yang dipilih
        String id_transaksi = model.getValueAt(selectedRowIndex, 0).toString();
        String id_barang = model.getValueAt(selectedRowIndex, 1).toString();
        String id_supplier = model.getValueAt(selectedRowIndex, 2).toString();
        String jumlah_barang = model.getValueAt(selectedRowIndex, 3).toString();

        // Masukkan data ke dalam JTextField
        jTextField1.setText(id_transaksi);
        // Atur nilai terpilih pada JComboBox
        jComboBox1.setSelectedItem(id_barang);
        jComboBox2.setSelectedItem(id_supplier);
        jTextField4.setText(jumlah_barang);;
    }
    }//GEN-LAST:event_jTable1MouseClicked

