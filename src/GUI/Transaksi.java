    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String id_transaksi = jTextField1.getText();
        String id_barang = jComboBox1.getSelectedItem().toString();
        String id_supplier = jComboBox2.getSelectedItem().toString();
        String jumlah_barang = jTextField4.getText();

    try {
        Connection kon = koneksi.koneksiDb();
        Statement st = kon.createStatement();
        String sql = "INSERT INTO transaksi (id_transaksi, id_barang, id_supplier, jumlah_barang) VALUES ('" + id_transaksi + "', '" + id_barang + "', '" + id_supplier + "', '" + jumlah_barang + "')";
        st.executeUpdate(sql);
        JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
        load_data(); // Setelah menambah data, muat ulang tabel
        IDOtomatis(); // Perbarui ID otomatis
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btnTambahActionPerformed

