    public void load_data() {
        try {
            Connection kon = koneksi.koneksiDb();
            Object header[] = {"ID TRANSAKSI", "ID BARANG", "ID SUPPLIER", "JUMLAH BARANG"};
            DefaultTableModel data = new DefaultTableModel(null, header);
            jTable1.setModel(data);
            String sql_data = "SELECT * FROM transaksi";

            Statement st = kon.createStatement();
            ResultSet rs = st.executeQuery(sql_data);
            while (rs.next()) {
                String d1 = rs.getString(1);
                String d2 = rs.getString(2);
                String d3 = rs.getString(3);
                String d4 = rs.getString(4);

                String d[] = {d1, d2, d3, d4};
                data.addRow(d);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void IDOtomatis() {
        try {
            Connection kon = koneksi.koneksiDb();
            Statement st = kon.createStatement();
            String sql_id = "SELECT * FROM transaksi ORDER BY id_transaksi DESC LIMIT 1";
            ResultSet rs = st.executeQuery(sql_id);
            if (rs.next()) {
                String id_transaksi = rs.getString("id_transaksi");
                int counter = Integer.parseInt(id_transaksi.replaceAll("[^0-9]", "")) + 1;
                String AN = String.format("%05d", counter); // Format ID dengan 5 digit angka
                jTextField1.setText("T" + AN);
            } else {
                jTextField1.setText("T00001");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void loadComboBoxData() {
        try {
        Connection kon = koneksi.koneksiDb();
        String sql_barang = "SELECT id_barang FROM master_barang";
        String sql_supplier = "SELECT id_supplier FROM master_supplier";

        Statement st1 = kon.createStatement();
        Statement st2 = kon.createStatement();
        ResultSet rsBarang = st1.executeQuery(sql_barang);
        ResultSet rsSupplier = st2.executeQuery(sql_supplier);

        while (rsBarang.next()) {
            jComboBox1.addItem(rsBarang.getString("id_barang"));
        }

        while (rsSupplier.next()) {
            jComboBox2.addItem(rsSupplier.getString("id_supplier"));
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }

